def sum(a, b):
    """
    This function takes two numbers as input and returns their sum.

    Parameters:
    a (int or float): The first number to be added.
    b (int or float): The second number to be added.

    Returns:
    int or float: The sum of the two input numbers.

    Example:
    >>> sum(3, 5)
    8
    >>> sum(2.5, 4.5)
    7.0
    """
    return a + b

